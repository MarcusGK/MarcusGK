
public class Computadora {
    String marca; //Atributo que define la marca de la computadora
    int cantidadMemoria; //Atributo que define la cantidad de memoria
    String caracteristicasProcesador; //Atributo que define las caracteristicas del procesador
    String sistemaOperativo; //Atributo que define el sistema operativo de la computadora
    double precio; //Atributo que define el precio de la co putadora
    
    public Computadora(String marca, int cantidadMemoria, String caracteristicasProcesador, String sistemaOperativo, double precio){
        this.marca=marca;
        this.cantidadMemoria=cantidadMemoria;
        this.caracteristicasProcesador=caracteristicasProcesador;
        this.sistemaOperativo=sistemaOperativo;
        this.precio=precio;
        
    }
    
}
